SELECT COALESCE("POSTTYPE", "POSTTYPE") AS "POSTTYPE", "TOTALPOSTS", "TOTALANSWERS", "TOTALCOMMENTS", "TOTALVOTES", "AVERAGESCORE"
FROM (SELECT ANY_VALUE("Name") AS "POSTTYPE", COUNT(*) AS "TOTALPOSTS", COUNT("Id1") AS "TOTALANSWERS", SUM("FD_COL_2") AS "TOTALCOMMENTS", SUM("FD_COL_3") AS "TOTALVOTES", AVG("Score") AS "AVERAGESCORE"
FROM "s1"
GROUP BY "Name"
ORDER BY 2 DESC NULLS FIRST) AS "t7"