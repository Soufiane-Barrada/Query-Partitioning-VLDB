SELECT COALESCE("Posts"."Id", "Posts"."Id") AS "POSTID", "Posts"."Title" AS "TITLE", "Posts"."CreationDate" AS "CREATIONDATE", "Posts"."Score" AS "SCORE", "Posts"."ViewCount" AS "VIEWCOUNT", "Users"."DisplayName" AS "OWNERDISPLAYNAME", "Users"."Reputation" AS "OWNERREPUTATION", "Posts"."AnswerCount" AS "ANSWERCOUNT", CASE WHEN "t0"."COMMENTCOUNT" IS NOT NULL THEN CAST("t0"."COMMENTCOUNT" AS BIGINT) ELSE 0 END AS "COMMENTCOUNT", CASE WHEN "t2"."LINKSCOUNT" IS NOT NULL THEN CAST("t2"."LINKSCOUNT" AS BIGINT) ELSE 0 END AS "LINKSCOUNT", CASE WHEN "t4"."HISTORYCOUNT" IS NOT NULL THEN CAST("t4"."HISTORYCOUNT" AS BIGINT) ELSE 0 END AS "HISTORYCOUNT"
FROM "STACK"."Posts"
INNER JOIN "STACK"."Users" ON "Posts"."OwnerUserId" = "Users"."Id"
LEFT JOIN (SELECT "PostId" AS "POSTID", COUNT(*) AS "COMMENTCOUNT"
FROM "STACK"."Comments"
GROUP BY "PostId") AS "t0" ON "Posts"."Id" = "t0"."POSTID"
LEFT JOIN (SELECT "PostId" AS "POSTID", COUNT(*) AS "LINKSCOUNT"
FROM "STACK"."PostLinks"
GROUP BY "PostId") AS "t2" ON "Posts"."Id" = "t2"."POSTID"
LEFT JOIN (SELECT "PostId" AS "POSTID", COUNT(*) AS "HISTORYCOUNT"
FROM "STACK"."PostHistory"
GROUP BY "PostId") AS "t4" ON "Posts"."Id" = "t4"."POSTID"
WHERE "Posts"."CreationDate" >= CAST((CAST('2024-10-01' AS DATE) - INTERVAL '1' YEAR) AS TIMESTAMP(0))