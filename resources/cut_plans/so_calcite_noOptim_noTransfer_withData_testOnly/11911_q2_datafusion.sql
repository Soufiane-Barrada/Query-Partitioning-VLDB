SELECT COALESCE("POSTTYPENAME", "POSTTYPENAME") AS "POSTTYPENAME", "TOTALPOSTS", "AVERAGESCORE", "UNIQUEUSERS"
FROM (SELECT ANY_VALUE("Name") AS "POSTTYPENAME", COUNT(*) AS "TOTALPOSTS", AVG("Score") AS "AVERAGESCORE", COUNT(DISTINCT "OwnerUserId") AS "UNIQUEUSERS"
FROM "s1"
GROUP BY "Name"
ORDER BY 2 DESC NULLS FIRST) AS "t3"