SELECT COALESCE("USERID", "USERID") AS "USERID", "DISPLAYNAME", "NUMBEROFPOSTS", "AVERAGEREPUTATION", "TOTALVOTES"
FROM (SELECT ANY_VALUE("Id") AS "USERID", "DISPLAYNAME", COUNT("Id0") AS "NUMBEROFPOSTS", AVG("Reputation") AS "AVERAGEREPUTATION", SUM("TOTALVOTES") AS "TOTALVOTES"
FROM "s1"
GROUP BY "Id", "DISPLAYNAME"
ORDER BY 3 DESC NULLS FIRST) AS "t5"