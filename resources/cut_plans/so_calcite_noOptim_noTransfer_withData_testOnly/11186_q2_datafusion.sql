SELECT COALESCE("POSTTYPE", "POSTTYPE") AS "POSTTYPE", "TOTALPOSTS", "AVERAGESCORE", "AVERAGEVIEWCOUNT", "UNIQUECONTRIBUTORS"
FROM (SELECT ANY_VALUE("Name") AS "POSTTYPE", COUNT(*) AS "TOTALPOSTS", AVG("Score") AS "AVERAGESCORE", AVG("ViewCount") AS "AVERAGEVIEWCOUNT", COUNT(DISTINCT "OwnerUserId") AS "UNIQUECONTRIBUTORS"
FROM "s1"
GROUP BY "Name"
ORDER BY 2 DESC NULLS FIRST) AS "t3"