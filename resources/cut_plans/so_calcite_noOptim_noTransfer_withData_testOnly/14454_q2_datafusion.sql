SELECT COALESCE("POSTTYPE", "POSTTYPE") AS "POSTTYPE", "TOTALPOSTS", "AVERAGESCORE", "MAXVIEWS", "TOTALCOMMENTS"
FROM (SELECT ANY_VALUE("Name") AS "POSTTYPE", COUNT(*) AS "TOTALPOSTS", AVG("Score") AS "AVERAGESCORE", MAX("ViewCount") AS "MAXVIEWS", COUNT(DISTINCT "Id1") AS "TOTALCOMMENTS"
FROM "s1"
GROUP BY "Name"
ORDER BY 2 DESC NULLS FIRST) AS "t3"