SELECT COALESCE("POSTTYPE", "POSTTYPE") AS "POSTTYPE", "TOTALPOSTS", "AVERAGESCORE", "TOTALUSERS", "AVERAGEVIEWCOUNT"
FROM (SELECT ANY_VALUE("Name") AS "POSTTYPE", COUNT(*) AS "TOTALPOSTS", AVG("Score") AS "AVERAGESCORE", COUNT(DISTINCT "OwnerUserId") AS "TOTALUSERS", AVG("ViewCount") AS "AVERAGEVIEWCOUNT"
FROM "s1"
GROUP BY "Name"
ORDER BY 2 DESC NULLS FIRST) AS "t3"