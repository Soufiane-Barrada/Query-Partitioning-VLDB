SELECT COALESCE("NATION", "NATION") AS "NATION", "REGION", "CUSTOMER_COUNT", "TOTAL_REVENUE"
FROM (SELECT ANY_VALUE("n_name") AS "NATION", ANY_VALUE("r_name") AS "REGION", COUNT(DISTINCT "c_custkey") AS "CUSTOMER_COUNT", SUM("o_totalprice") AS "TOTAL_REVENUE"
FROM "s1"
GROUP BY "n_name", "r_name"
ORDER BY 4 DESC NULLS FIRST) AS "t3"