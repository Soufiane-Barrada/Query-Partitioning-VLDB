SELECT COALESCE("AVERAGE_SUPPLIER_COST", "AVERAGE_SUPPLIER_COST") AS "AVERAGE_SUPPLIER_COST", "TOTAL_SALES_VALUE"
FROM (SELECT AVG("AVG_SUPPLY_COST") AS "AVERAGE_SUPPLIER_COST", SUM("TOTAL_ORDER_VALUE") AS "TOTAL_SALES_VALUE"
FROM (SELECT MAX("r_name") AS "REGION_NAME", AVG("TOTAL_SUPPLY_COST") AS "AVG_SUPPLY_COST", "TOTAL_ORDER_VALUE"
FROM "s1"
GROUP BY "P_PARTKEY", "P_NAME", "P_BRAND", "CUSTOMER_COUNT", "TOTAL_ORDER_VALUE") AS "t8"
GROUP BY "REGION_NAME"
ORDER BY 2 DESC NULLS FIRST) AS "t11"