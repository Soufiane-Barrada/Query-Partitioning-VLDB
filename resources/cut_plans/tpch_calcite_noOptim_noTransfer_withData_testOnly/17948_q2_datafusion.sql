SELECT COALESCE("P_BRAND", "P_BRAND") AS "P_BRAND", "SUPPLIER_COUNT", "TOTAL_AVAILABLE_QUANTITY", "AVERAGE_SUPPLY_COST"
FROM (SELECT "P_BRAND", COUNT(DISTINCT "ps_suppkey") AS "SUPPLIER_COUNT", SUM("ps_availqty") AS "TOTAL_AVAILABLE_QUANTITY", AVG("ps_supplycost") AS "AVERAGE_SUPPLY_COST"
FROM "s1"
GROUP BY "P_BRAND"
ORDER BY 2 DESC NULLS FIRST) AS "t2"