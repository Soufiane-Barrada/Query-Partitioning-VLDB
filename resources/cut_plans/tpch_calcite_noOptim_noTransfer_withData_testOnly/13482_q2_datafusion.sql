SELECT COALESCE("NATION", "NATION") AS "NATION", "REGION", "SUPPLIER_COUNT", "TOTAL_AVAILABILITY", "AVERAGE_SUPPLY_COST"
FROM (SELECT ANY_VALUE("n_name") AS "NATION", ANY_VALUE("r_name") AS "REGION", COUNT(DISTINCT "s_suppkey") AS "SUPPLIER_COUNT", SUM("ps_availqty") AS "TOTAL_AVAILABILITY", AVG("ps_supplycost") AS "AVERAGE_SUPPLY_COST"
FROM "s1"
GROUP BY "n_name", "r_name"
ORDER BY 2, 1) AS "t3"