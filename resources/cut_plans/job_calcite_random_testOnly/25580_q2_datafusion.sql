SELECT COALESCE("t9"."TITLE", "t9"."TITLE") AS "TITLE", "t9"."PRODUCTION_YEAR", "t9"."ACTOR_NAME", "t9"."INFO_TYPE_COUNT"
FROM (SELECT "s1"."TITLE", "s1"."PRODUCTION_YEAR", "t6"."NAME", ANY_VALUE("t6"."NAME") AS "ACTOR_NAME", COUNT(DISTINCT "s1"."info_type_id") AS "INFO_TYPE_COUNT"
FROM (SELECT "person_id" AS "PERSON_ID", "name" AS "NAME", "id" AS "ID"
FROM "IMDB"."aka_name"
WHERE "name" ILIKE '%Smith%') AS "t6"
INNER JOIN "s1" ON "t6"."PERSON_ID" = "s1"."person_id"
GROUP BY "t6"."NAME", "s1"."TITLE", "s1"."PRODUCTION_YEAR"
ORDER BY "s1"."PRODUCTION_YEAR" DESC NULLS FIRST, 4) AS "t9"