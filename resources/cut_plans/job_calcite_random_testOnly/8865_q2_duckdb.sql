SELECT COALESCE("ACTOR_NAME", "ACTOR_NAME") AS "ACTOR_NAME", "MOVIE_TITLE", "PRODUCTION_YEAR", "COMPANY_TYPES", "KEYWORDS", "name"
FROM (SELECT ANY_VALUE("name") AS "ACTOR_NAME", ANY_VALUE("title") AS "MOVIE_TITLE", "PRODUCTION_YEAR", LISTAGG(DISTINCT "kind0", "FD_COL_4") AS "COMPANY_TYPES", LISTAGG(DISTINCT "keyword", "FD_COL_4") AS "KEYWORDS", "name"
FROM "s1"
GROUP BY "name", "title", "PRODUCTION_YEAR"
ORDER BY "PRODUCTION_YEAR" DESC NULLS FIRST, "name") AS "t4"