SELECT COALESCE("t12"."DISPLAYNAME", "t12"."DISPLAYNAME") AS "DISPLAYNAME", "t12"."REPUTATION", "t12"."TOTALBADGES", "t12"."TOTALPOSTS", "t12"."TOTALQUESTIONS", "t12"."TOTALANSWERS", "t12"."TOTALVIEWS"
FROM (SELECT "t4"."DISPLAYNAME", "t4"."REPUTATION", "t4"."TOTALBADGES", "t10"."TOTALPOSTS", "t10"."TOTALQUESTIONS", "t10"."TOTALANSWERS", "t10"."TOTALVIEWS"
FROM (SELECT "Users"."Id" AS "ID", "Users"."DisplayName" AS "DISPLAYNAME", "Users"."Reputation" AS "REPUTATION", "t2"."TOTALBADGES"
FROM "STACK"."Users"
LEFT JOIN (SELECT "UserId", COUNT(*) AS "TOTALBADGES"
FROM "STACK"."Badges"
GROUP BY "UserId") AS "t2" ON "Users"."Id" = "t2"."UserId"
ORDER BY "Users"."Reputation" DESC NULLS FIRST, "t2"."TOTALBADGES" DESC NULLS FIRST
FETCH NEXT 10 ROWS ONLY) AS "t4"
INNER JOIN ((SELECT "UserId", COUNT(*) AS "TOTALBADGES"
FROM "STACK"."Badges"
GROUP BY "UserId") AS "t5" INNER JOIN (SELECT "Users1"."Id" AS "ID", "Users1"."DisplayName" AS "DISPLAYNAME", "s1"."TOTALPOSTS", "s1"."TOTALQUESTIONS", "s1"."TOTALANSWERS", "s1"."TOTALVIEWS"
FROM (SELECT "t8"."ID"
FROM (SELECT "Users0"."Id" AS "ID", "Users0"."DisplayName" AS "DISPLAYNAME", "Users0"."Reputation" AS "REPUTATION", "t6"."TOTALBADGES"
FROM "STACK"."Users" AS "Users0"
LEFT JOIN (SELECT "UserId", COUNT(*) AS "TOTALBADGES"
FROM "STACK"."Badges"
GROUP BY "UserId") AS "t6" ON "Users0"."Id" = "t6"."UserId"
ORDER BY "Users0"."Reputation" DESC NULLS FIRST, "t6"."TOTALBADGES" DESC NULLS FIRST
FETCH NEXT 10 ROWS ONLY) AS "t8") AS "t9"
INNER JOIN ("s1" INNER JOIN "STACK"."Users" AS "Users1" ON "s1"."OWNERUSERID" = "Users1"."Id") ON "t9"."ID" = "Users1"."Id") AS "t10" ON "t5"."UserId" = "t10"."ID") ON "t4"."ID" = "t10"."ID"
ORDER BY "t4"."REPUTATION" DESC NULLS FIRST) AS "t12"