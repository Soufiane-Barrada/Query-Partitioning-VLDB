SELECT COALESCE("USERID", "USERID") AS "USERID", "DISPLAYNAME", "TOTALPOSTS", "AVERAGESCORE", "TOTALVOTES"
FROM (SELECT ANY_VALUE("Id0") AS "USERID", "DisplayName" AS "DISPLAYNAME", COUNT("Id00") AS "TOTALPOSTS", CASE WHEN AVG("Score") IS NOT NULL THEN CAST(AVG("Score") AS INTEGER) ELSE 0 END AS "AVERAGESCORE", CASE WHEN SUM("Id") IS NOT NULL THEN CAST(SUM("Id") AS INTEGER) ELSE 0 END AS "TOTALVOTES"
FROM "s1"
GROUP BY "Id0", "DisplayName"
ORDER BY 3 DESC NULLS FIRST, 4 DESC NULLS FIRST) AS "t3"