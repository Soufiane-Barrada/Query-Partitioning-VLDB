SELECT COALESCE("POSTTYPE", "POSTTYPE") AS "POSTTYPE", "POSTCOUNT", "AVERAGESCORE", "TOTALCOMMENTS"
FROM (SELECT "Name", ANY_VALUE("Name") AS "POSTTYPE", COUNT(*) AS "POSTCOUNT", AVG("Score") AS "AVERAGESCORE", SUM("COMMENTCOUNT_") AS "TOTALCOMMENTS"
FROM "s1"
GROUP BY "Name"
ORDER BY 3 DESC NULLS FIRST) AS "t2"