SELECT COALESCE("DISPLAYNAME", "DISPLAYNAME") AS "DISPLAYNAME", "USERID", "POSTSCREATED", "TOTALVOTES", "TOTALCOMMENTS", "FIRSTPOSTDATE", "LASTPOSTDATE"
FROM (SELECT "DisplayName" AS "DISPLAYNAME", "USERID", "POSTSCREATED", "TOTALVOTES", "TOTALCOMMENTS", "FIRSTPOSTDATE", "LASTPOSTDATE"
FROM "s1"
ORDER BY "TOTALVOTES" DESC NULLS FIRST, "POSTSCREATED" DESC NULLS FIRST) AS "t7"