SELECT COALESCE("POSTTYPENAME", "POSTTYPENAME") AS "POSTTYPENAME", "POSTCOUNT", "AVERAGESCORE", "UNIQUEUSERCOUNT"
FROM (SELECT "Name", ANY_VALUE("Name") AS "POSTTYPENAME", COUNT(*) AS "POSTCOUNT", AVG("Score") AS "AVERAGESCORE", COUNT(DISTINCT "OwnerUserId") AS "UNIQUEUSERCOUNT"
FROM "s1"
GROUP BY "Name"
ORDER BY 3 DESC NULLS FIRST) AS "t1"