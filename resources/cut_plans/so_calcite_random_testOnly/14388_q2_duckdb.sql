SELECT COALESCE("t5"."POSTTYPE", "t5"."POSTTYPE") AS "POSTTYPE", "t5"."TOTALPOSTS", "t5"."TOTALANSWERS", "t5"."TOTALCOMMENTS", "t5"."TOTALVOTES", "t5"."AVERAGESCORE"
FROM (SELECT "PostTypes"."Name", ANY_VALUE("PostTypes"."Name") AS "POSTTYPE", COUNT(*) AS "TOTALPOSTS", COUNT("t1"."Id") AS "TOTALANSWERS", SUM(CASE WHEN "t2"."COMMENTCOUNT" IS NOT NULL THEN CAST("t2"."COMMENTCOUNT" AS BIGINT) ELSE 0 END) AS "TOTALCOMMENTS", SUM(CASE WHEN "s1"."VOTECOUNT" IS NOT NULL THEN CAST("s1"."VOTECOUNT" AS BIGINT) ELSE 0 END) AS "TOTALVOTES", AVG("Posts"."Score") AS "AVERAGESCORE"
FROM "STACK"."Posts"
INNER JOIN "STACK"."PostTypes" ON "Posts"."PostTypeId" = "PostTypes"."Id"
LEFT JOIN (SELECT *
FROM "STACK"."Posts"
WHERE CAST("PostTypeId" AS INTEGER) = 2) AS "t1" ON "Posts"."Id" = "t1"."ParentId"
LEFT JOIN (SELECT "PostId", COUNT(*) AS "COMMENTCOUNT"
FROM "STACK"."Comments"
GROUP BY "PostId") AS "t2" ON "Posts"."Id" = "t2"."PostId"
LEFT JOIN "s1" ON "Posts"."Id" = "s1"."PostId"
GROUP BY "PostTypes"."Name"
ORDER BY 3 DESC NULLS FIRST) AS "t5"