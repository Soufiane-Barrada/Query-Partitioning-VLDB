SELECT COALESCE("t3"."POSTTYPE", "t3"."POSTTYPE") AS "POSTTYPE", "t3"."TOTALPOSTS", "t3"."AVERAGESCORE", "t3"."TOTALCOMMENTS"
FROM (SELECT "s1"."Name", ANY_VALUE("s1"."Name") AS "POSTTYPE", COUNT(*) AS "TOTALPOSTS", AVG("s1"."Score") AS "AVERAGESCORE", SUM(CASE WHEN "t0"."COMMENTCOUNT" IS NOT NULL THEN CAST("t0"."COMMENTCOUNT" AS BIGINT) ELSE 0 END) AS "TOTALCOMMENTS"
FROM "s1"
LEFT JOIN (SELECT "PostId", COUNT(*) AS "COMMENTCOUNT"
FROM "STACK"."Comments"
GROUP BY "PostId") AS "t0" ON "s1"."Id" = "t0"."PostId"
GROUP BY "s1"."Name"
ORDER BY 3 DESC NULLS FIRST) AS "t3"