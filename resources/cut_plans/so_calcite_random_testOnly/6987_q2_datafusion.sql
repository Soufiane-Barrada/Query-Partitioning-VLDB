SELECT COALESCE("t9"."USERID", "t9"."USERID") AS "USERID", "t9"."DISPLAYNAME", "t9"."TOTALBADGES", "t9"."TOTALPOSTS", "t9"."QUESTIONS", "t9"."ANSWERS", "t9"."TOTALSCORE", "t9"."TOTALVIEWCOUNT"
FROM (SELECT "s1"."USERID", "s1"."DISPLAYNAME", "s1"."TOTALBADGES", "s1"."TOTALPOSTS", "s1"."QUESTIONS", "s1"."ANSWERS", "s1"."TOTALSCORE", "s1"."TOTALVIEWCOUNT"
FROM "s1"
INNER JOIN (SELECT "OwnerUserId"
FROM "STACK"."Posts"
GROUP BY "OwnerUserId") AS "t7" ON "s1"."USERID" = "t7"."OwnerUserId"
ORDER BY "s1"."TOTALSCORE" DESC NULLS FIRST) AS "t9"