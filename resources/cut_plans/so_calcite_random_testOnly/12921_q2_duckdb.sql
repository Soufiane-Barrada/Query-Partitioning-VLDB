SELECT COALESCE("POSTTYPE", "POSTTYPE") AS "POSTTYPE", "TOTALPOSTS", "TOTALSCORE", "TOTALVIEWS", "AVERAGEUSERREPUTATION", "TOTALUSERS"
FROM (SELECT "Name", ANY_VALUE("Name") AS "POSTTYPE", COUNT(*) AS "TOTALPOSTS", SUM("Score") AS "TOTALSCORE", SUM("ViewCount") AS "TOTALVIEWS", AVG("Reputation") AS "AVERAGEUSERREPUTATION", COUNT(DISTINCT "Id1") AS "TOTALUSERS"
FROM "s1"
GROUP BY "Name"
ORDER BY 3 DESC NULLS FIRST) AS "t1"