SELECT COALESCE("t2"."POSTTYPE", "t2"."POSTTYPE") AS "POSTTYPE", "t2"."TOTALPOSTS", "t2"."AVERAGESCORE", "t2"."TOTALCOMMENTS"
FROM (SELECT "s1"."Name", ANY_VALUE("s1"."Name") AS "POSTTYPE", COUNT(*) AS "TOTALPOSTS", AVG("s1"."Score") AS "AVERAGESCORE", SUM("t0"."COMMENTCOUNT") AS "TOTALCOMMENTS"
FROM "s1"
LEFT JOIN (SELECT "PostId", COUNT(*) AS "COMMENTCOUNT"
FROM "STACK"."Comments"
GROUP BY "PostId") AS "t0" ON "s1"."Id" = "t0"."PostId"
GROUP BY "s1"."Name"
ORDER BY 3 DESC NULLS FIRST) AS "t2"