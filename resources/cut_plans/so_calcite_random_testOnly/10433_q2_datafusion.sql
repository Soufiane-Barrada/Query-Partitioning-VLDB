SELECT COALESCE("POSTTYPE", "POSTTYPE") AS "POSTTYPE", "POSTCOUNT", "TOTALUPVOTES", "TOTALDOWNVOTES", "TOTALCOMMENTS", "TOTALVIEWS", "AVERAGESCORE"
FROM (SELECT "Name", ANY_VALUE("Name") AS "POSTTYPE", COUNT(*) AS "POSTCOUNT", SUM("FD_COL_1") AS "TOTALUPVOTES", SUM("FD_COL_2") AS "TOTALDOWNVOTES", SUM("FD_COL_3") AS "TOTALCOMMENTS", SUM("FD_COL_4") AS "TOTALVIEWS", AVG("Score") AS "AVERAGESCORE"
FROM "s1"
GROUP BY "Name"
ORDER BY 8 DESC NULLS FIRST) AS "t5"