SELECT COALESCE("ACTOR_NAME", "ACTOR_NAME") AS "ACTOR_NAME", "AVG_CO_STARS_OVER_ALL_MOVIES", "TOTAL_MOVIES", "MOVIE_TITLES"
FROM (SELECT "ACTOR_NAME", "AVG_CO_STARS_OVER_ALL_MOVIES", COUNT(DISTINCT "MOVIE_TITLE") AS "TOTAL_MOVIES", LISTAGG(DISTINCT "MOVIE_TITLE", "FD_COL_3") AS "MOVIE_TITLES"
FROM "s1"
GROUP BY "ACTOR_NAME", "AVG_CO_STARS_OVER_ALL_MOVIES"
ORDER BY 3 DESC NULLS FIRST) AS "t13"