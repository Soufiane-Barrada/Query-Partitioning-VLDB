SELECT COALESCE("TITLE", "TITLE") AS "TITLE", "PRODUCTION_YEAR", "ACTOR_NAME", "INFO_TYPE_COUNT"
FROM (SELECT "TITLE", "PRODUCTION_YEAR", ANY_VALUE("NAME") AS "ACTOR_NAME", COUNT(DISTINCT "info_type_id") AS "INFO_TYPE_COUNT"
FROM "s1"
GROUP BY "TITLE", "PRODUCTION_YEAR", "NAME"
ORDER BY "PRODUCTION_YEAR" DESC NULLS FIRST, 3) AS "t12"