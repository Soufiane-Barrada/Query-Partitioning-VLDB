SELECT COALESCE("t5"."POSTTYPE", "t5"."POSTTYPE") AS "POSTTYPE", "t5"."TOTALPOSTS", "t5"."TOTALANSWERS", "t5"."TOTALCOMMENTS", "t5"."TOTALVOTES", "t5"."AVERAGESCORE"
FROM (SELECT "PostTypes"."Name", ANY_VALUE("PostTypes"."Name") AS "POSTTYPE", COUNT(*) AS "TOTALPOSTS", COUNT("s1"."Id") AS "TOTALANSWERS", SUM(CASE WHEN "t1"."COMMENTCOUNT" IS NOT NULL THEN CAST("t1"."COMMENTCOUNT" AS BIGINT) ELSE 0 END) AS "TOTALCOMMENTS", SUM(CASE WHEN "t2"."VOTECOUNT" IS NOT NULL THEN CAST("t2"."VOTECOUNT" AS BIGINT) ELSE 0 END) AS "TOTALVOTES", AVG("Posts0"."Score") AS "AVERAGESCORE"
FROM "STACK"."Posts" AS "Posts0"
INNER JOIN "STACK"."PostTypes" ON "Posts0"."PostTypeId" = "PostTypes"."Id"
LEFT JOIN "s1" ON "Posts0"."Id" = "s1"."ParentId"
LEFT JOIN (SELECT "PostId", COUNT(*) AS "COMMENTCOUNT"
FROM "STACK"."Comments"
GROUP BY "PostId") AS "t1" ON "Posts0"."Id" = "t1"."PostId"
LEFT JOIN (SELECT "PostId", COUNT(*) AS "VOTECOUNT"
FROM "STACK"."Votes"
GROUP BY "PostId") AS "t2" ON "Posts0"."Id" = "t2"."PostId"
GROUP BY "PostTypes"."Name"
ORDER BY 3 DESC NULLS FIRST) AS "t5"