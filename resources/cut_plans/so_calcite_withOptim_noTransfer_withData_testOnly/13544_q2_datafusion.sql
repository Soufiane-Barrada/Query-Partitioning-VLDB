SELECT COALESCE("POSTTYPE", "POSTTYPE") AS "POSTTYPE", "TOTALPOSTS", "TOTALUSERS", "TOTALCOMMENTS", "TOTALVOTES", "Id"
FROM (SELECT ANY_VALUE("Name") AS "POSTTYPE", COUNT(DISTINCT "Id0") AS "TOTALPOSTS", COUNT(DISTINCT "Id1") AS "TOTALUSERS", COUNT(DISTINCT "Id2") AS "TOTALCOMMENTS", COUNT(DISTINCT "Id3") AS "TOTALVOTES", "Id"
FROM "s1"
GROUP BY "Id", "Name"
ORDER BY "Id") AS "t3"