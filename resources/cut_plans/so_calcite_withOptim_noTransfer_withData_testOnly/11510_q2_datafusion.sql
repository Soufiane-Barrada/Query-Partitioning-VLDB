SELECT COALESCE("t5"."POSTTYPENAME", "t5"."POSTTYPENAME") AS "POSTTYPENAME", "t5"."POSTCOUNT", "t5"."AVGSCORE", "t5"."USERCOUNT", "t5"."VOTETYPENAME", "t5"."VOTECOUNT"
FROM (SELECT "t3"."POSTTYPENAME", "t3"."POSTCOUNT", "t3"."AVGSCORE", "t3"."USERCOUNT", "t1"."VOTETYPENAME", "t1"."VOTECOUNT"
FROM (SELECT ANY_VALUE("VoteTypes"."Name") AS "VOTETYPENAME", COUNT(*) AS "VOTECOUNT"
FROM "STACK"."VoteTypes"
INNER JOIN "STACK"."Votes" AS "Votes0" ON "VoteTypes"."Id" = "Votes0"."VoteTypeId"
GROUP BY "VoteTypes"."Name") AS "t1",
(SELECT ANY_VALUE("Name") AS "POSTTYPENAME", COUNT(*) AS "POSTCOUNT", AVG("Score") AS "AVGSCORE", COUNT(DISTINCT "UserId") AS "USERCOUNT"
FROM "s1"
GROUP BY "Name") AS "t3"
ORDER BY "t3"."POSTTYPENAME", "t1"."VOTETYPENAME") AS "t5"