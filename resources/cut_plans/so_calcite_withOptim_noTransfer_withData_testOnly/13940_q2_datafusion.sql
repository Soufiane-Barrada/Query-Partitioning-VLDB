SELECT COALESCE("t5"."USERID", "t5"."USERID") AS "USERID", "t5"."DISPLAYNAME", "t5"."TOTALPOSTS", "t5"."TOTALQUESTIONS", "t5"."TOTALANSWERS", "t5"."TOTALVIEWS", "t5"."AVERAGESCORE", "t5"."TOTALBADGES"
FROM (SELECT "t2"."USERID", "t2"."DISPLAYNAME", "t2"."TOTALPOSTS", "t2"."TOTALQUESTIONS", "t2"."TOTALANSWERS", "t2"."TOTALVIEWS", "t2"."AVERAGESCORE", CASE WHEN "t3"."TOTALBADGES" IS NOT NULL THEN CAST("t3"."TOTALBADGES" AS BIGINT) ELSE 0 END AS "TOTALBADGES"
FROM (SELECT ANY_VALUE("Id") AS "USERID", "DISPLAYNAME", COUNT("Id0") AS "TOTALPOSTS", SUM("FD_COL_3") AS "TOTALQUESTIONS", SUM("FD_COL_4") AS "TOTALANSWERS", SUM("ViewCount") AS "TOTALVIEWS", AVG("Score") AS "AVERAGESCORE"
FROM "s1"
GROUP BY "Id", "DISPLAYNAME") AS "t2"
LEFT JOIN (SELECT "UserId", COUNT(*) AS "TOTALBADGES"
FROM "STACK"."Badges"
GROUP BY "UserId") AS "t3" ON "t2"."USERID" = "t3"."UserId"
ORDER BY "t2"."TOTALPOSTS" DESC NULLS FIRST
FETCH NEXT 100 ROWS ONLY) AS "t5"