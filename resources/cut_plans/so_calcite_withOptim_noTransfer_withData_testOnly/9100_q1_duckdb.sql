SELECT COALESCE("t1"."ID", "t1"."ID") AS "ID", "t1"."DISPLAYNAME", "t1"."REPUTATION", "t1"."TOTALBADGES", "t2"."UserId" AS "USERID", "t2"."TOTALBADGES" AS "TOTALBADGES0", "t9"."ID" AS "ID0", "t9"."DISPLAYNAME" AS "DISPLAYNAME0", "t9"."TOTALPOSTS", "t9"."TOTALQUESTIONS", "t9"."TOTALANSWERS", "t9"."TOTALVIEWS"
FROM (SELECT "Users"."Id" AS "ID", "Users"."DisplayName" AS "DISPLAYNAME", "Users"."Reputation" AS "REPUTATION", "t"."TOTALBADGES"
FROM "STACK"."Users"
LEFT JOIN (SELECT "UserId", COUNT(*) AS "TOTALBADGES"
FROM "STACK"."Badges"
GROUP BY "UserId") AS "t" ON "Users"."Id" = "t"."UserId"
ORDER BY "Users"."Reputation" DESC NULLS FIRST, "t"."TOTALBADGES" DESC NULLS FIRST
FETCH NEXT 10 ROWS ONLY) AS "t1"
INNER JOIN ((SELECT "UserId", COUNT(*) AS "TOTALBADGES"
FROM "STACK"."Badges"
GROUP BY "UserId") AS "t2" INNER JOIN (SELECT "Users1"."Id" AS "ID", "Users1"."DisplayName" AS "DISPLAYNAME", "t8"."TOTALPOSTS", "t8"."TOTALQUESTIONS", "t8"."TOTALANSWERS", "t8"."TOTALVIEWS"
FROM (SELECT "t5"."ID"
FROM (SELECT "Users0"."Id" AS "ID", "Users0"."DisplayName" AS "DISPLAYNAME", "Users0"."Reputation" AS "REPUTATION", "t3"."TOTALBADGES"
FROM "STACK"."Users" AS "Users0"
LEFT JOIN (SELECT "UserId", COUNT(*) AS "TOTALBADGES"
FROM "STACK"."Badges"
GROUP BY "UserId") AS "t3" ON "Users0"."Id" = "t3"."UserId"
ORDER BY "Users0"."Reputation" DESC NULLS FIRST, "t3"."TOTALBADGES" DESC NULLS FIRST
FETCH NEXT 10 ROWS ONLY) AS "t5") AS "t6"
INNER JOIN ((SELECT "OwnerUserId" AS "OWNERUSERID", COUNT(*) AS "TOTALPOSTS", SUM(CASE WHEN CAST("PostTypeId" AS INTEGER) = 1 THEN 1 ELSE 0 END) AS "TOTALQUESTIONS", SUM(CASE WHEN CAST("PostTypeId" AS INTEGER) = 2 THEN 1 ELSE 0 END) AS "TOTALANSWERS", SUM("ViewCount") AS "TOTALVIEWS"
FROM "STACK"."Posts"
GROUP BY "OwnerUserId") AS "t8" INNER JOIN "STACK"."Users" AS "Users1" ON "t8"."OWNERUSERID" = "Users1"."Id") ON "t6"."ID" = "Users1"."Id") AS "t9" ON "t2"."UserId" = "t9"."ID") ON "t1"."ID" = "t9"."ID"