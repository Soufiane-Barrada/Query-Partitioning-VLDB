SELECT COALESCE("POSTID", "POSTID") AS "POSTID", "TITLE", "CREATIONDATE", "COMMENTCOUNT", "UPVOTECOUNT", "DOWNVOTECOUNT", "BADGECOUNT", "LASTEDITDATE"
FROM (SELECT ANY_VALUE("Id") AS "POSTID", "TITLE", "CREATIONDATE", COUNT("Id0") AS "COMMENTCOUNT", SUM("FD_COL_4") AS "UPVOTECOUNT", SUM("FD_COL_5") AS "DOWNVOTECOUNT", COUNT(DISTINCT "Id2") AS "BADGECOUNT", MAX("CreationDate2") AS "LASTEDITDATE"
FROM "s1"
GROUP BY "Id", "TITLE", "CREATIONDATE"
ORDER BY "CREATIONDATE" DESC NULLS FIRST) AS "t6"