SELECT COALESCE("POSTTYPE", "POSTTYPE") AS "POSTTYPE", "TOTALPOSTS", "AVERAGESCORE", "MAXVIEWS", "TOTALCOMMENTS"
FROM (SELECT "Name", ANY_VALUE("Name") AS "POSTTYPE", COUNT(*) AS "TOTALPOSTS", AVG("Score0") AS "AVERAGESCORE", MAX("ViewCount") AS "MAXVIEWS", COUNT(DISTINCT "Id") AS "TOTALCOMMENTS"
FROM "s1"
GROUP BY "Name"
ORDER BY 3 DESC NULLS FIRST) AS "t1"