SELECT COALESCE("USERID", "USERID") AS "USERID", "DisplayName" AS "DISPLAYNAME", "NUMBEROFPOSTS", "AVERAGEREPUTATION", "TOTALVOTES"
FROM (SELECT "Id", "DisplayName", ANY_VALUE("Id") AS "USERID", COUNT("Id0") AS "NUMBEROFPOSTS", AVG("Reputation") AS "AVERAGEREPUTATION", SUM("TOTALVOTES") AS "TOTALVOTES"
FROM "s1"
GROUP BY "Id", "DisplayName"
ORDER BY 4 DESC NULLS FIRST) AS "t3"