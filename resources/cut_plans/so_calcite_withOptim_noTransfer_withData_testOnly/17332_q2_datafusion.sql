SELECT COALESCE("POSTID", "POSTID") AS "POSTID", "TITLE", "OWNERDISPLAYNAME", "CREATIONDATE", "SCORE", "VIEWCOUNT", "COMMENTCOUNT", "VOTECOUNT"
FROM (SELECT "Id" AS "POSTID", "Title" AS "TITLE", "DisplayName" AS "OWNERDISPLAYNAME", "CreationDate" AS "CREATIONDATE", "Score" AS "SCORE", "ViewCount" AS "VIEWCOUNT", "COMMENTCOUNT_" AS "COMMENTCOUNT", "VOTECOUNT"
FROM "s1"
ORDER BY "CreationDate" DESC NULLS FIRST
FETCH NEXT 10 ROWS ONLY) AS "t5"