SELECT COALESCE("NATION", "NATION") AS "NATION", "REGION", "CUSTOMER_COUNT", "TOTAL_REVENUE"
FROM (SELECT "n_name", "r_name", ANY_VALUE("n_name") AS "NATION", ANY_VALUE("r_name") AS "REGION", COUNT(DISTINCT "c_custkey") AS "CUSTOMER_COUNT", SUM("o_totalprice") AS "TOTAL_REVENUE"
FROM "s1"
GROUP BY "r_name", "n_name"
ORDER BY 6 DESC NULLS FIRST) AS "t2"