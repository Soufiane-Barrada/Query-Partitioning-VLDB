SELECT COALESCE("R_NAME", "R_NAME") AS "R_NAME", "REVENUE", "ORDER_COUNT", "AVG_SUPPLIER_COST"
FROM (SELECT "R_NAME", SUM("FD_COL_1") AS "REVENUE", COUNT(DISTINCT "O_ORDERKEY") AS "ORDER_COUNT", ANY_VALUE("FD_COL_3") AS "AVG_SUPPLIER_COST"
FROM "s1"
GROUP BY "R_NAME"
ORDER BY 2 DESC NULLS FIRST) AS "t10"