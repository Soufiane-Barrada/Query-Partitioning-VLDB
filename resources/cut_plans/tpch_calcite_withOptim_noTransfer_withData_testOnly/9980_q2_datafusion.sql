SELECT COALESCE("AVERAGE_SUPPLIER_COST", "AVERAGE_SUPPLIER_COST") AS "AVERAGE_SUPPLIER_COST", "TOTAL_SALES_VALUE"
FROM (SELECT "REGION_NAME", AVG("AVG_SUPPLY_COST") AS "AVERAGE_SUPPLIER_COST", SUM("TOTAL_ORDER_VALUE") AS "TOTAL_SALES_VALUE"
FROM (SELECT "p_partkey", "p_name", "p_brand", "CUSTOMER_COUNT", "TOTAL_ORDER_VALUE", MAX("r_name") AS "REGION_NAME", AVG("TOTAL_SUPPLY_COST") AS "AVG_SUPPLY_COST"
FROM "s1"
GROUP BY "p_partkey", "p_name", "p_brand", "CUSTOMER_COUNT", "TOTAL_ORDER_VALUE") AS "t7"
GROUP BY "REGION_NAME"
ORDER BY 3 DESC NULLS FIRST) AS "t9"