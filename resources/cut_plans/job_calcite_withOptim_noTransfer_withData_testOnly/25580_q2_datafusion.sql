SELECT COALESCE("TITLE", "TITLE") AS "TITLE", "PRODUCTION_YEAR", "ACTOR_NAME", "INFO_TYPE_COUNT"
FROM (SELECT "TITLE", "PRODUCTION_YEAR", "NAME", ANY_VALUE("NAME") AS "ACTOR_NAME", COUNT(DISTINCT "info_type_id") AS "INFO_TYPE_COUNT"
FROM "s1"
GROUP BY "NAME", "TITLE", "PRODUCTION_YEAR"
ORDER BY "PRODUCTION_YEAR" DESC NULLS FIRST, 4) AS "t9"