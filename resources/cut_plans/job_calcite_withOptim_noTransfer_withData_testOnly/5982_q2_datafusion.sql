SELECT COALESCE("ACTOR_NAME", "ACTOR_NAME") AS "ACTOR_NAME", "MOVIE_TITLE", "COMPANY_TYPE", "MOVIE_KEYWORD", "PERSON_INFO", "TOTAL_CASTS", "title"
FROM (SELECT "ACTOR_NAME", "MOVIE_TITLE", "COMPANY_TYPE", "MOVIE_KEYWORD", "PERSON_INFO", "TOTAL_CASTS", "title"
FROM "s1"
ORDER BY "TOTAL_CASTS" DESC NULLS FIRST, "title"
FETCH NEXT 50 ROWS ONLY) AS "t5"