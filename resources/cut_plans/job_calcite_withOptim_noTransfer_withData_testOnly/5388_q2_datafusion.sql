SELECT COALESCE("ACTOR_NAME", "ACTOR_NAME") AS "ACTOR_NAME", "MOVIE_TITLE", "PRODUCTION_YEAR", "COMPANY_TYPE", "MOVIE_KEYWORD", "PERSON_INFO"
FROM (SELECT "name" AS "ACTOR_NAME", "title" AS "MOVIE_TITLE", "production_year" AS "PRODUCTION_YEAR", "kind" AS "COMPANY_TYPE", "keyword" AS "MOVIE_KEYWORD", "info" AS "PERSON_INFO"
FROM "s1"
ORDER BY "production_year" DESC NULLS FIRST, "name"
FETCH NEXT 100 ROWS ONLY) AS "t3"