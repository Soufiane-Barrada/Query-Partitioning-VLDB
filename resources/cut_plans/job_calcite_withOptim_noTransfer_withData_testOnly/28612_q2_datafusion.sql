SELECT COALESCE("MOVIE_TITLE", "MOVIE_TITLE") AS "MOVIE_TITLE", "PRODUCTION_YEAR", "ACTORS", "KEYWORDS", "COMPANIES"
FROM (SELECT "MOVIE_TITLE", "PRODUCTION_YEAR", LISTAGG(DISTINCT "ACTOR_NAME", "FD_COL_4") AS "ACTORS", LISTAGG(DISTINCT "MOVIE_KEYWORD", "FD_COL_4") AS "KEYWORDS", LISTAGG(DISTINCT "FD_COL_6", "FD_COL_4") AS "COMPANIES"
FROM "s1"
GROUP BY "MOVIE_ID", "MOVIE_TITLE", "PRODUCTION_YEAR"
ORDER BY "PRODUCTION_YEAR" DESC NULLS FIRST, "MOVIE_TITLE") AS "t6"