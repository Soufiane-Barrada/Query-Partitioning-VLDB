SELECT COALESCE("MOVIE_TITLE", "MOVIE_TITLE") AS "MOVIE_TITLE", "ACTOR_NAME", "CAST_TYPE", "COMPANY_NAME", "MOVIE_INFO", "MOVIE_KEYWORD", "LATEST_YEAR"
FROM (SELECT "MOVIE_TITLE", "ACTOR_NAME", "CAST_TYPE", "COMPANY_NAME", "MOVIE_INFO", "MOVIE_KEYWORD", "LATEST_YEAR"
FROM "s1"
ORDER BY "LATEST_YEAR" DESC NULLS FIRST, "MOVIE_TITLE"
FETCH NEXT 100 ROWS ONLY) AS "t10"